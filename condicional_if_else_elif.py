#Condicional IF , ELSE e ELIF

numero1 = 10
numero2 = 15

if(numero1 > numero2):
    print("Numero1 é maior que Numero2")
else:
    print("Numero1 é menor que Numero2")



if(numero1 == numero2):
    print("Numero1 é igual a Numero2")
elif(numero1 > numero2):
    print("Numero1 é maior que Numero2")
else:
    print("Numero1 é menor que Numero2")